model {

  # priors ------------------------------------------------------------------
  tau ~ dgamma(0.01, 0.01)
  b0 ~ dnorm(0, 0.01)
  b1 ~ dnorm(0, 0.01)
  b2 ~ dnorm(0, 0.01)
  
  # likelihood --------------------------------------------------------------
  
  for (i in 1:Nsample) {
    Y[i] ~ dnorm(mu[i], tau)
    mu[i] <- b0 + b1 * X1[i] + b2 * X2[i]
  }
  
}